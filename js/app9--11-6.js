const num = +prompt('Enter any number: ')

if (num % 2 === 0) {
    alert(num + ' is an even number');
    document.write(num + ' is an even number' + '<br/>');
}
else {
    alert(num + ' is an odd number');
    document.write(num + ' is an odd number' + '<br/>');
}

