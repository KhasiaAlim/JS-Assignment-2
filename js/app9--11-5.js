const num = +prompt('Enter any number:');

if (num % 3 === 0) {
    alert(num + ' is divisible by 3');
    document.write(num + ' is divisible by 3' + '<br/>');
}
else {
    alert(num + ' is not divisible by 3');
    document.write(num + ' is not divisible by 3' + '<br/>');
}

